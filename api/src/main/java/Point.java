/**
 * Represents a point in a plane identified by its coordinates.
 */
public interface Point {
    /**
     * Returns the x value.
     *
     * @return the x value
     */
    double x();

    /**
     * Returns the y value.
     *
     * @return the y value
     */
    double y();
}
